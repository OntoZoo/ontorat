<h3 class="header_darkred">News</h3>
<ul>
  <li><strong>Oct 7, 2014:</strong> Dr. Jie Zheng introduced Ontorat in her<a href="http://icbo14.com/sessions/obo-technical-workshop-hands-on-with-open-biological-and-biomedical-ontologies/"> OBO Technical Workshop</a> talk in ICBO-2014. </li>
  <li><strong>Oct 6, 2014:</strong> Dr. Jie Zheng introduced Ontorat in her<a href="http://icbo14.com/sessions/obo-tutorial-getting-things-done-with-open-biological-and-biomedical-ontologies/"> OBO Tutorial</a> talk in ICBO-2014. See more information in <a href="http://ncorwiki.buffalo.edu/index.php/2014_ICBO_OBO_Tutorial">the NCOR wiki</a>. </li>
  <li><strong>July 2014</strong>: Updated Ontorat <a href="tutorial/index.php">Tutorial</a> and added <a href="designtemplates/index.php">Design Templates</a> pages by Jie Zheng and Oliver He. </li>
  <li><strong>July 7, 2013</strong>:  Ontorat was presented in the <a href="http://www2.unb.ca/csas/data/ws/semantic-trilogy-2013/tutorials.html">OBO Tutorial</a>  in ICBO-2013. See more information in <a href="http://ncorwiki.buffalo.edu/index.php/2013_ICBO_OBO_tutorial">the NCOR wiki</a>. </li>
  <li><strong>Nov 13, 2012</strong>: Allen updated the Ontorat by using OWLAPI to parse the input ontology. The information provided by OWLAPI can allow the program to automatically extract detailed ontology term information, such as term labels, IDs, and relations. This feature was not available before, so previous version has to ask users to provide all the needed information. By doing this, the speed is somehow slower than before. However, in general, the speed is still fast. The final speed depends on many factors, including the ontology size to be parsed. </li>
  <li> <strong>July, 2012</strong>: A flash talk and a poster presentation were given by Oliver He at the ICBO 2012 conference: Xiang Z, Lin Y, He Y. Ontorat web server for automatic ontology term generation and annotations. <em>International Conference on Biomedical Ontologies</em> (ICBO 2012), University of Graz, Austria, July 2012. [<a href="http://ontorat.hegroup.org/Ontorat_ICBO2012.pdf">One page abstract</a>][<a href="http://ontorat.hegroup.org/Ontorat_poster_ICBO2012.pdf">Poster</a>][<a href="http://ontorat.hegroup.org/Ontorat_FlashTalk.pdf">Flash Talk</a>] </li>
  <li><strong>August, 2011</strong>: Ontorat server was created online by Allen Xiang and Oliver He. </li>
