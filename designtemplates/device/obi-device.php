<h3><span class="header_darkred">  Design Pattern, Template, and Example: OBI Device</span></h3>
<p><strong>Task:</strong> Add device terms into Ontology for Biomedical Investigations (OBI) for ENCODE project based on OBI device pattern. </p>
<p>&nbsp;</p>
<p><span id="toc1"><strong>1. Design Pattern:</strong></span></p>
<blockquote>
  <p>This use case study aims to use Ontorat to generate new  OBI device terms to be used for an ENCODE project. An OBI device design pattern is generated. An Ontorat template is also generated accordingly. </p>
  <p><strong>Design pattern diagram: </strong></p>
 	<p><img src="pattern_device.jpg" alt="OBI device pattern" width="700"></p>
   <p>&nbsp;  </p>
</blockquote>
<p>&nbsp;</p>
<p><span id="toc1"><strong>2. Ontorat Template Files: </strong></span></p>
<blockquote>
  <p>Ontorat setting file: <a href="Ontorat_settings_device.txt">Ontorat_settings_device.txt</a> (.txt format)</p>
  <p>Ontorat input template file: <a href="Ontorat_input_device_template.xlsx">Ontorat_input_device_template.xlsx</a> (.xlsx format)</p>
  <p>&nbsp;</p>
</blockquote>
<p><span id="toc1"><strong>3. Ontorat Example: </strong></span></p>
<blockquote>
  <p><strong>Introduction: </strong>This example aims to add cell line cells based on the CLO. The input data is a list of cell line cells collected from the Japan Rikens institute. </p>
  <p>Ontorat input data file (based on template): <a href="Ontorat_input_device.xlsx">Ontorat_input_device.xlsx</a> <a href="ontoRat_input_assay.xlsx"></a> (.xlsx format)</p>
  <p>Ontorat output file: <a href="Ontorat_output_device.owl">Ontorat_output_device.owl</a> <a href="Ontorat_output_cellline.owl"></a> (.owl format)</p>
  <p>Ontorat output new IDs file when adding new ontology classes: <a href="Ontorat_output_newIds_device.xlsx">Ontorat_output_newIds_device.xlsx</a>  (.xlsx format)</p>
  <p>&nbsp;</p>
</blockquote>
