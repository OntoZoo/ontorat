<h3><span class="header_darkred">Collection of  Ontology Design Patterns,    Templates, and Examples</span> </h3>
<p>&nbsp;</p>
<p>Here we provide a list of ontology design patterns (ODPs), corresponding Ontorat templates, and examples for ontology developers' and domain experts' reference. </p>
<p><strong>Introduction: </strong></p>
<ul>
  <li> The ontology design patterns include a specific diagram that lays out the inforamtion about the design pattern.</li>
  <li>The Ontorat template includes two files for each use case:
    <ul>
      <li>The setting file specifies how terms are related. </li>
      <li>The input template file specifies the input format in an Excel file. </li>
    </ul>
  </li>
  <li>An  example includes several files:
    <ul>
      <li>Input data file in Excel format. </li>
      <li>Output owl file.</li>
      <li>Updated input data file with the newly generated ontology IDs included. </li>
      </ul>
  </li>
  </ul>
<p><strong>Collection of design patterns, templates, and examples :</strong></p>
<ol>
  <li><a href="assay/obi-assay.php">Assays (OBI) </a></li>
  <li><a href="cellline/clo-celllinecell.php">Cell line cells (CLO)</a> </li>
  <li><a href="device/obi-device.php">Devices (OBI)</a> </li>
  <li><a href="biobank/index.php">Entities in Biobank (Biobank Ontology) </a></li>
  <li><a href="vaccine/vo-vaccine.php"> Licensed animal vaccines (VO)</a></li>
  <li><a href="strain/bcgo-mousestrain.php">Mouse strains (BCGO)</a></li>
  </ol>
<p>Note that the names inside parentheses are the namespaces of target ontologies used for the use case studies.</p>
<p>&nbsp; </p>
<p><strong>Note: </strong>  If you have any design patterns to share, please <a href="../contactus.php">contact us</a>.  </p>
